    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        tableDetails();
        int checkId = 0;
        String id = jTextField1.getText();
        String month = jLabel3.getText();
        try {
            Connection con = ConnectionHandler.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from member where id='"+id+"'");
            while(rs.next()) {
                checkId = 1;
                jTextField1.setEditable(false);
                jTextField2.setText(rs.getString(2));
                jTextField3.setText(rs.getString(4));
                jTextField4.setText(rs.getString(9));
            }
            if (checkId == 0) 
                JOptionPane.showMessageDialog(null, "Can not found member with that id");
            
            ResultSet rs1 = st.executeQuery("select * from payment inner join member where payment.month='"+month+"' and payment.id='"+id+"' and member.id='"+id+ "'");
            while(rs1.next()) {
                jButton3.setVisible(false);
                JOptionPane.showMessageDialog(null, "Payment already done");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String id = jTextField1.getText();
        String month = jLabel3.getText();
        String amount = jTextField4.getText();
        try {
            Connection con = ConnectionHandler.getCon();
            PreparedStatement ps = con.prepareStatement("insert into payment values(?, ?, ?)");
            ps.setString(1, id);
            ps.setString(2, month);
            ps.setString(3, amount);
            ps.executeUpdate();
            tableDetails();
            JOptionPane.showMessageDialog(null, "Payment updated!");
            setVisible(false);
            new payment().setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

