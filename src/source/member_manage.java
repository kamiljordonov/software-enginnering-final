    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String id = jTextField7.getText();
        String name = jTextField1.getText();
        String surname = jTextField2.getText();
        String mobile = jTextField3.getText();
        String gender = jTextField8.getText();
        String time = jTextField9.getText();
        String genId = jTextField4.getText();
        String age = jTextField5.getText();
        String price = jTextField6.getText();
        
        try {
            Connection con = ConnectionHandler.getCon();
            PreparedStatement ps = con.prepareStatement("update member set name=?, surname=?, mobile_number=?, gender=?, gym_time=?, generated_id=?, age=?, price=? where id=?");
            ps.setString(1, name);
            ps.setString(2, surname);
            ps.setString(3, mobile);
            ps.setString(4, gender);
            ps.setString(5, time);
            ps.setString(6, genId);
            ps.setString(7, age);
            ps.setString(8, price);
            ps.setString(9, id);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Updated!");
            setVisible(false);
            new member_manage().setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int checkId = 0;
        
        String id = jTextField7.getText();
        try {
            Connection con = ConnectionHandler.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from member where id='"+id+"'");
            while(rs.next()) {
                checkId = 1;
                jTextField7.setEditable(false);
                jTextField1.setText(rs.getString(2));
                jTextField2.setText(rs.getString(3));
                jTextField3.setText(rs.getString(4));
                jTextField8.setText(rs.getString(5));
                jTextField8.setEditable(false);
                jTextField9.setText(rs.getString(6));
                jTextField9.setEditable(false);
                jTextField4.setText(rs.getString(7));
                jTextField5.setText(rs.getString(8));
                jTextField6.setText(rs.getString(9));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Can not found member with such id");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

